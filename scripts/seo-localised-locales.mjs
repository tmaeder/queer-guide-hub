#!/usr/bin/env node
/**
 * Detects which i18n locales have been meaningfully translated by comparing
 * each locale JSON's string values against en.json. A locale is "localised"
 * iff the ratio of strings that differ from English is at least
 * LOCALISED_THRESHOLD (default 5 %).
 *
 * Output: writes functions/_lib/localisedLocales.ts with the resolved set,
 * which the middleware consumes to gate hreflang emission and to noindex
 * unlocalised locale-prefixed URLs.
 *
 * Re-run whenever a locale gets translated; the file is committed so the
 * middleware doesn't have to do filesystem I/O at request time.
 *
 * Usage:
 *   node scripts/seo-localised-locales.mjs            # writes the file
 *   node scripts/seo-localised-locales.mjs --check    # report only, no write
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { resolve, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const ROOT = resolve(dirname(__filename), '..');
const LOCALES_DIR = resolve(ROOT, 'src/i18n/locales');
const OUTPUT = resolve(ROOT, 'functions/_lib/localisedLocales.ts');

const LOCALES = ['en', 'es', 'fr', 'de', 'pt', 'it', 'ru', 'zh', 'ja', 'ko', 'ar'];
const LOCALISED_THRESHOLD = 0.05; // 5 % of strings must differ from English.

const dryRun = process.argv.includes('--check');

function flatten(obj, prefix = '') {
  const out = {};
  for (const [k, v] of Object.entries(obj)) {
    const key = prefix ? `${prefix}.${k}` : k;
    if (v && typeof v === 'object' && !Array.isArray(v)) {
      Object.assign(out, flatten(v, key));
    } else if (typeof v === 'string') {
      out[key] = v;
    }
  }
  return out;
}

const en = flatten(JSON.parse(readFileSync(resolve(LOCALES_DIR, 'en.json'), 'utf8')));
const enKeys = Object.keys(en);

const results = [];
for (const code of LOCALES) {
  if (code === 'en') {
    results.push({ code, localised: true, ratio: 1, total: enKeys.length, differ: enKeys.length });
    continue;
  }
  let local;
  try {
    local = flatten(JSON.parse(readFileSync(resolve(LOCALES_DIR, `${code}.json`), 'utf8')));
  } catch (e) {
    results.push({ code, localised: false, ratio: 0, total: 0, differ: 0, error: String(e) });
    continue;
  }
  let differ = 0;
  let compared = 0;
  for (const k of enKeys) {
    if (!(k in local)) continue;
    compared++;
    if (local[k] !== en[k]) differ++;
  }
  const ratio = compared ? differ / compared : 0;
  results.push({
    code,
    localised: ratio >= LOCALISED_THRESHOLD,
    ratio,
    total: compared,
    differ,
  });
}

console.log('Locale translation report');
console.log('=========================');
for (const r of results) {
  const pct = (r.ratio * 100).toFixed(1);
  const badge = r.localised ? '✓ localised' : '✗ unlocalised';
  console.log(`  ${r.code}  ${badge}  ${r.differ}/${r.total} keys differ from en (${pct}%)`);
}
const localisedCodes = results.filter((r) => r.localised).map((r) => r.code);
console.log('');
console.log(`Localised locales: ${localisedCodes.join(', ')}`);

if (dryRun) {
  console.log('--check mode — file not written.');
  process.exit(0);
}

const fileContents = `// AUTO-GENERATED by scripts/seo-localised-locales.mjs. Do not edit by hand.
// Lists locales whose translation files differ from en.json by at least 5 %
// of strings. Used by functions/_middleware.ts to gate hreflang emission and
// to emit noindex for locale-prefixed URLs whose content is still effectively
// English (P3.1 of the SEO remediation). Re-run the script after a locale
// gets translated.

export const LOCALISED_LOCALES: readonly string[] = ${JSON.stringify(localisedCodes)} as const;

export function isLocaleLocalised(locale: string): boolean {
  return LOCALISED_LOCALES.includes(locale);
}
`;

writeFileSync(OUTPUT, fileContents);
console.log(`Wrote ${OUTPUT}`);
