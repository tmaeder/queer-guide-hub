// Generates the repo-root index.d.ts the design-sync converter reads as the
// package's type entry. Synth-entry mode has no shipped .d.ts, so we emit one:
// tsc --emitDeclarationOnly writes types/ (gitignored), and this script
// re-exports exactly the canonical components from design-sync.config.json's
// componentSrcMap — subcomponents (CardHeader, DialogTrigger, …) stay out of
// the card list but remain in the JS bundle.
//
// Run via the config's buildCmd chain; both types/ and index.d.ts are
// regenerated on every sync.
import { readFileSync, writeFileSync } from 'fs';

const cfg = JSON.parse(readFileSync('design-sync.config.json', 'utf8'));
const byFile = new Map();
for (const [name, src] of Object.entries(cfg.componentSrcMap)) {
  if (!src) continue;
  const dts = './' + src.replace(/^src\//, 'types/src/').replace(/\.tsx$/, '');
  if (!byFile.has(dts)) byFile.set(dts, []);
  byFile.get(dts).push(name);
}
const lines = ['// generated by .design-sync/gen-types-entry.mjs — do not edit', ''];
for (const [dts, names] of [...byFile.entries()].sort()) {
  lines.push(`export { ${names.sort().join(', ')} } from '${dts}';`);
}
writeFileSync('index.d.ts', lines.join('\n') + '\n');
console.error(`index.d.ts: ${byFile.size} files, ${[...byFile.values()].flat().length} components`);
