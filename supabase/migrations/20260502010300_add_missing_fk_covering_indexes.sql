-- Add covering indexes for every foreign-key column in public.* that doesn't
-- already have one. Closes the 109+ unindexed_foreign_keys advisor flags
-- (count includes some composite-key FKs the advisor doesn't surface).
--
-- Each index is CREATE INDEX IF NOT EXISTS so re-runs are no-ops. A few
-- duplicate index names (e.g. messages.sender_id has two FKs to two referenced
-- tables) are harmless — the second CREATE silently no-ops.
--
-- Tradeoff: each new index adds write amplification to inserts/updates on
-- the table. FK columns are typically UUID/int8 (small footprint per row).
-- Net effect is positive: every parent-row delete or PK change previously
-- triggered a full child-table scan; now it does an index lookup.
--
-- Already applied to prod via Supabase MCP on 2026-05-02.
-- Ref: docs/index-cleanup-batches.md (Section 2 — unindexed FKs).

